package JavaBasics;

public class GreatOrLess {
	public static void main(String[] args) {
		int a = 110;
		if (a < 10) {
			System.out.println("a is less then 10");
		}
		else if (a == 10 ) {
			System.out.println("a is equal to 10");
		}
		else {
			System.out.println("a is greater then 10");
		}
	}
}
